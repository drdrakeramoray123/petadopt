const birdBreeds = [""];

typeElement.addEventListener("change", (event) => {
    const type = event.target.value;
    const breedOptions = document.querySelector("#pet-breed");
    breedOptions.innerHTML = "";
    if (type === "dog") {
        createOptions(breedOptions, dogBreeds);
    } else if (type === "cat") {
        createOptions(breedOptions, catBreeds);
    } else if (type === "fish") {
        createOptions(breedOptions, fishBreeds);
    } else if (type === "bird") {
        createOptions(breedOptions, birdBreeds);
    }
});

function createOptions(breedOptions, breedList) {
    breedList.forEach((breed) => {
        const option = document.createElement("option");
        option.value = breed;
        option.textContent = snakeToSpace(breed);
        breedOptions.append(option);
    });
}

function snakeToSpace(string) {
    return string
        .replace(/_/g, " ")
        .split(" ")
        .map((word) => {
            return word.charAt(0).toUpperCase() + word.slice(1);
        })
        .join(" ");
}
